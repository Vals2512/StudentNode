package model;

/**
 * Enumeration to represent possible genders.
 * This enumeration includes the values FEMALE, MALE, and OTHER.
 */

public enum EGender {
    /**
     * Makes reference to a female
     */
    FEMALE,
    /**
     * Makes reference to a male
     */
    MALE,

    /**
     * Makes reference to other gender
     */
    OTHER
}
